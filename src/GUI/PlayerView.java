    public void setPlayer(Player p){
        playerModel = p;
        name.setText(p.getName());
        level.setText(Integer.toString(p.getLevels()));
        combatLevel.setText(Integer.toString(p.getCombatLevel()));
        enemy.setText(p.getEnemy().getName());
        
        if(playerModel.canISteal())
            canISteal.setText("Si");
        else
            canISteal.setText("No");
        
        if(playerModel instanceof CultistPlayer)
            sectario.setText(((CultistPlayer)playerModel).getCultist().toString());
        else
            sectario.setText("No");
        
        if(playerModel.getPendingBadConsequence() != null)
            pendingBadConsequence.setPendingBadConsequence(playerModel.getPendingBadConsequence());
        
        fillTreasurePanel(visibleTreasures, playerModel.getVisibleTreasures());
        fillTreasurePanel(hiddenTreasures, playerModel.getHiddenTreasures());
        repaint();
        revalidate();
    }
    
    private void fillTreasurePanel(JPanel aPanel, ArrayList<Treasure> aList) {
        // Se elimina la informaci칩n antigua
        aPanel.removeAll();
        // Se recorre la lista de tesoros construyendo y a침adiendo sus vistas al panel
        for (Treasure t : aList) {
            TreasureView aTreasureView = new TreasureView();
            aTreasureView.setTreasure (t);
            aTreasureView.setVisible (true);
            aPanel.add (aTreasureView);
        }
        // Se fuerza la actualizaci칩n visual del panel
        aPanel.repaint();
        aPanel.revalidate();
    }
    
    public Player getPlayer(){
        return playerModel;
    }
    
    private ArrayList<Treasure> getSelectedTreasures(JPanel aPanel) {
        // Se recorren los tesoros que contiene el panel,
        //almacenando en un vector aquellos que est치n seleccionados.
        //Finalmente se devuelve dicho vector.

        TreasureView tv;
        ArrayList<Treasure> output = new ArrayList();
        for (Component c : aPanel.getComponents()) {
            tv = (TreasureView) c;
        if ( tv.isSelected() )
            output.add ( tv.getTreasure() );
        }
        return output;
    }
    
